'''
Fazer o algoritmo que pergunte 3 números e pergunte a média aritimética entre estes 3 números.
'''

#Input
num1 = float(input("Informe um número"))
num2 = float(input("Informe um segundo número"))
num3 = float(input("Informe um terceiro número"))

#Cálculo

med = (num1 + num2 + num3)/3

#Output
print("A média do valor apresentado é de:", med)